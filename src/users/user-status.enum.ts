export enum UserStatus{
    ACTIVE = 'ACTIVE',
    INACTIVE = 'INACTIVE',
    SUSPENDED = 'SUSPENDED',
    DELETED = 'DELETED',
}
// ACTIVE: Đại diện cho trạng thái người dùng đang hoạt động.
// INACTIVE: Đại diện cho trạng thái người dùng không hoạt động.
// SUSPENDED: Đại diện cho trạng thái người dùng bị đình chỉ.
// DELETED: Đại diện cho trạng thái người dùng bị xóa.